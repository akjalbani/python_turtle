import turtle
wn = turtle.Screen()
wn.bgcolor("light green")
wn.title("Turtle")
myturtle = turtle.Turtle()

myturtle.forward(100)
myturtle.left(90)

myturtle.forward(100)
myturtle.left(90)

myturtle.forward(100)
myturtle.left(90)

myturtle.forward(100)
myturtle.left(90)

turtle.done()
